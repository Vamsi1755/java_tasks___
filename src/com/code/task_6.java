package com.code;

public class task_6 {

	public static void main(String[] args) {
		int a = 15;
		int result;
		System.out.println(a);
		
	    result = a++;
	    System.out.println(result);
	    
	    result = ++a;
	    System.out.println(result);
	    
	    result = a--;
	    System.out.println(result);
	    
	    result = --a;
	    System.out.println(result);

	}

}
